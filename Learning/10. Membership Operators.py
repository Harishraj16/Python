# Membership operator

'''
#Definition

Membership operators in Python are used to determine
whether a particular value belongs to a sequence or a collection. 
There are two membership operators:
    
    1. in.
    2. not in.

'''

a=[10,25,45,55,88]
print(45 in a)
print(45  not in a)

print(22 in  a)
print(22 not in a)