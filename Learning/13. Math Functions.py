#Math Functions

import math as mt  

print("Sq.root:",int(mt.sqrt(4)))
print("Ceil:",mt.ceil(1.55))
print("Floor:",mt.floor(1.55))
print("Factorial:",mt.factorial(5))
print("Absolute: ",mt.fabs(-5))
print("Power:",mt.pow(2, 2))
print("Log:",mt.log2(2))
print("Log:",mt.log10(2))
print("pi:",mt.pi)
print("e:",mt.e)